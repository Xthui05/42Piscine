#include <unistd.h>

void	ft_print_comb2(void)
{
	char	i;
	char	j;

	i = '0';
	while (i <= '9')
	{
		j = '0';
		while (j <= '9')
		{
			write(1, &i, 1);
			write(1, &j, 1);
			if (!(i == '9' && j == '9'))
			{
				write(1, ", ", 2);
			}
			j++;
		}
		i++;
	}
}

int	main(void)
{
	ft_print_comb2();
	write(1, "\n", 1);
	return (0);
}
